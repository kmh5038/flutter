class MovieInfo {
  final String rank;
  final String title;
  final String releaseDate;
  final String totalAudience;
  final String posterUrl;
  final String runtime;

  MovieInfo({
    required this.rank,
    required this.title,
    required this.releaseDate,
    required this.totalAudience,
    required this.posterUrl,
    required this.runtime
  });
}